#include<iostream>
using namespace std;
int main() {
    int v, a, b;
    cin >> a >> b >> v;

    v -= a;
    int day = 1;

    day += v / (a - b);
    if (v % (a - b) != 0) {
        day++;
    }

    cout << day;
    return 0;
}
